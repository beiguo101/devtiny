type MessageKey = keyof typeof messages['zh-CN']

const state = reactive({
  locale: (localStorage.getItem('devtiny.locale') as Locale) || 'zh-CN'
})

export const locale = computed(() => state.locale)

export function setLocale(locale: Locale) {
  state.locale = locale
  localStorage.setItem('devtiny.locale', locale)
}

export function t(key: MessageKey): string {
  return messages[state.locale][key]
}

export function statusLabel(status: string) {
  const key = `status${status.charAt(0).toUpperCase()}${status.slice(1)}` as MessageKey
  return messages[state.locale][key] || status
}
